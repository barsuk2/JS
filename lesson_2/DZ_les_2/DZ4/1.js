'use strict';

const multiplication_func = function (arg1, arg2) {
    return arg1 * arg2;
}

const summation_func = function (arg1, arg2) {
    return arg1 + arg2;
}

const subtraction_func = function (arg1, arg2) {
    return arg1 - arg2;
}

const division_func = function (arg1, arg2) {
    return arg1 / arg2;
}

alert(multiplication_func(10, 20))
alert(summation_func(10, 20))
alert(subtraction_func(10, 20))
alert(division_func(10, 20))

